'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useSession, signOut } from 'next-auth/react';
import { useState, useEffect, useCallback } from 'react';

const navItems = [
  { href: '/', icon: DashboardIcon, label: 'Dashboard' },
  { href: '/tasks', icon: TasksIcon, label: 'Tasks' },
  { href: '/requests', icon: RequestsIcon, label: 'Requests', badge: true },
  { href: '/clients', icon: ClientsIcon, label: 'Clients' },
  { href: '/pipeline', icon: PipelineIcon, label: 'Pipeline' },
  { href: '/prospector', icon: ProspectorIcon, label: 'Prospector' },
  { href: '/websites', icon: WebsitesIcon, label: 'Websites' },
  { href: '/seo', icon: SEOIcon, label: 'SEO' },
  { href: '/proposals', icon: ProposalIcon, label: 'Proposals' },
  { href: '/reports', icon: ReportsIcon, label: 'Reports' },
  { href: '/revenue', icon: RevenueIcon, label: 'Revenue' },
];

export default function Sidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();
  const [newRequestCount, setNewRequestCount] = useState(0);

  const fetchNewRequests = useCallback(async () => {
    try {
      const res = await fetch('/api/change-requests?status=new');
      if (res.ok) {
        const data = await res.json();
        setNewRequestCount(data.length);
      }
    } catch {}
  }, []);

  useEffect(() => {
    if (!session) return;
    fetchNewRequests();
    const interval = setInterval(fetchNewRequests, 30000); // poll every 30s
    return () => clearInterval(interval);
  }, [session, fetchNewRequests]);

  // Don't render sidebar on login or public request pages (but keep it on /requests admin page)
  if (pathname === '/login' || (pathname.startsWith('/request/') && !pathname.startsWith('/requests'))) return null;

  const userName = session?.user?.name || session?.user?.email?.split('@')[0] || '';
  const userInitial = userName.charAt(0).toUpperCase();

  return (
    <nav className="fixed left-0 top-0 h-screen w-20 glass-subtle flex flex-col items-center py-6 gap-2 z-50">
      <div className="w-12 h-12 rounded-xl bg-accent/10 flex items-center justify-center mb-6 overflow-hidden p-1.5">
        {/* eslint-disable-next-line @next/next/no-img-element */}
        <img
          src="/images/Logo.png"
          alt="TruePath Studios"
          className="w-full h-full object-contain brightness-0 invert"
        />
      </div>

      {navItems.map(({ href, icon: Icon, label, badge }) => {
        const isActive = href === '/' ? pathname === '/' : pathname.startsWith(href);
        const showBadge = badge && newRequestCount > 0;
        return (
          <Link
            key={href}
            href={href}
            className={`w-12 h-12 rounded-xl flex items-center justify-center transition-all duration-200 group relative
              ${isActive ? 'glass-active glow-accent' : 'hover:bg-white/10'}`}
            title={label}
          >
            <Icon className={`w-5 h-5 ${isActive ? 'text-accent' : 'text-white/50 group-hover:text-white/80'}`} />
            {showBadge && (
              <span className="absolute -top-0.5 -right-0.5 min-w-[18px] h-[18px] rounded-full bg-red-500 text-white text-[10px] font-bold flex items-center justify-center px-1 shadow-lg shadow-red-500/30 animate-pulse">
                {newRequestCount}
              </span>
            )}
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              {label}{showBadge ? ` (${newRequestCount} new)` : ''}
            </span>
          </Link>
        );
      })}

      {/* Spacer */}
      <div className="flex-1" />

      {/* Settings (admin only) */}
      {(session?.user as any)?.role === 'admin' && (
        <Link
          href="/settings/users"
          className={`w-12 h-12 rounded-xl flex items-center justify-center transition-all duration-200 group relative
            ${pathname.startsWith('/settings') ? 'glass-active glow-accent' : 'hover:bg-white/10'}`}
          title="Settings"
        >
          <SettingsIcon className={`w-5 h-5 ${pathname.startsWith('/settings') ? 'text-accent' : 'text-white/50 group-hover:text-white/80'}`} />
          <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
            Settings
          </span>
        </Link>
      )}

      {/* User section */}
      {session?.user && (
        <div className="flex flex-col items-center gap-2">
          <Link
            href="/settings/account"
            className="w-10 h-10 rounded-xl bg-white/10 flex items-center justify-center group relative hover:bg-white/15 transition-all"
            title="My Account"
          >
            <span className="text-white/70 text-sm font-medium">{userInitial}</span>
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              {userName} · My Account
            </span>
          </Link>
          <button
            onClick={() => signOut({ callbackUrl: '/login' })}
            className="w-10 h-10 rounded-xl flex items-center justify-center hover:bg-white/10 transition-all duration-200 group relative"
            title="Sign Out"
          >
            <LogoutIcon className="w-4 h-4 text-white/30 group-hover:text-white/60" />
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              Sign Out
            </span>
          </button>
        </div>
      )}
    </nav>
  );
}

