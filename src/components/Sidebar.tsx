'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useSession, signOut } from 'next-auth/react';

const navItems = [
  { href: '/', icon: DashboardIcon, label: 'Dashboard' },
  { href: '/tasks', icon: TasksIcon, label: 'Tasks' },
  { href: '/clients', icon: ClientsIcon, label: 'Clients' },
  { href: '/websites', icon: WebsitesIcon, label: 'Websites' },
  { href: '/revenue', icon: RevenueIcon, label: 'Revenue' },
  { href: '/agents', icon: AgentsIcon, label: 'Agents' },
];

export default function Sidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();

  // Don't render sidebar on login page
  if (pathname === '/login') return null;

  const userName = session?.user?.name || session?.user?.email?.split('@')[0] || '';
  const userInitial = userName.charAt(0).toUpperCase();

  return (
    <nav className="fixed left-0 top-0 h-screen w-20 glass-subtle flex flex-col items-center py-6 gap-2 z-50">
      <div className="w-10 h-10 rounded-xl bg-accent/20 flex items-center justify-center mb-6">
        <span className="text-accent font-bold text-lg">MC</span>
      </div>

      {navItems.map(({ href, icon: Icon, label }) => {
        const isActive = href === '/' ? pathname === '/' : pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className={`w-12 h-12 rounded-xl flex items-center justify-center transition-all duration-200 group relative
              ${isActive ? 'glass-active glow-accent' : 'hover:bg-white/10'}`}
            title={label}
          >
            <Icon className={`w-5 h-5 ${isActive ? 'text-accent' : 'text-white/50 group-hover:text-white/80'}`} />
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              {label}
            </span>
          </Link>
        );
      })}

      {/* Spacer */}
      <div className="flex-1" />

      {/* User section */}
      {session?.user && (
        <div className="flex flex-col items-center gap-2">
          <div className="w-10 h-10 rounded-xl bg-white/10 flex items-center justify-center group relative">
            <span className="text-white/70 text-sm font-medium">{userInitial}</span>
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              {userName}
            </span>
          </div>
          <button
            onClick={() => signOut({ callbackUrl: '/login' })}
            className="w-10 h-10 rounded-xl flex items-center justify-center hover:bg-white/10 transition-all duration-200 group relative"
            title="Sign Out"
          >
            <LogoutIcon className="w-4 h-4 text-white/30 group-hover:text-white/60" />
            <span className="absolute left-16 bg-white/15 backdrop-blur-xl text-white text-xs px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 pointer-events-none whitespace-nowrap transition-opacity">
              Sign Out
            </span>
          </button>
        </div>
      )}
    </nav>
  );
}

